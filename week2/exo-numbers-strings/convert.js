/* Ecrivez un programme qui convertira les nombres suivants en binaire, octal et hexadécimal (utiliser .toString() pour cela):

10
15
16
5005
52390903 */

let a = 10
let b = 15
let c = 16
let d = 5005
let e = 52390903

console.log(`10 = ${a.toString(2)} en binaire, ${a.toString(8)} en octal et ${a.toString(16)} en hexadécimal.`)
console.log(`15 = ${b.toString(2)} en binaire, ${b.toString(8)} en octal et ${b.toString(16)} en hexadécimal.`)
console.log(`16 = ${c.toString(2)} en binaire, ${c.toString(8)} en octal et ${c.toString(16)} en hexadécimal.`)
console.log(`5005 = ${d.toString(2)} en binaire, ${d.toString(8)} en octal et ${d.toString(16)} en hexadécimal.`)
console.log(`52390903 = ${e.toString(2)} en binaire, ${e.toString(8)} en octal et ${e.toString(16)} en hexadécimal.`)
